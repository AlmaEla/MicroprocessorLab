DATA SEGMENT
    NUM1 DW 000AH
    NUM2 DW 0004H
    GCD DW ?
    DATA ENDS
CODE SEGMENT
    ASSUME CS:CODE,DS:DATA
    START:MOV AX,DATA
    MOV DS,AX
    MOV AX,NUM1
    MOV BX,NUM2 
    
    COMPARE:CMP AX,BX
    JE EXIT
    JB SWAP
    
    DIVIDE:MOV DX,0H
    DIV BX
    CMP DX,0
    JE EXIT
    MOV AX,DX
    JMP COMPARE 
    
    SWAP:XCHG AX,BX
    JMP DIVIDE
    
    EXIT:MOV GCD,BX
    MOV AH,4CH
    INT 21H
    CODE ENDS
END START
