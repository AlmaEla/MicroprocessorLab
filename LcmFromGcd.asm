DATA SEGMENT
    NUM1 DW 000AH
    NUM2 DW 0004H
    GCD  DW ?
    LCM  DW ?
DATA ENDS

CODE SEGMENT
ASSUME CS:CODE, DS:DATA
START:
    MOV AX, DATA
    MOV DS, AX

    MOV AX, NUM1
    MOV BX, NUM2

COMPARE:
    CMP AX, BX
    JE  GCD_FOUND
    JB  SWAP_AX_BX

DIVIDE:
    MOV DX, 0
    DIV BX
    CMP DX, 0
    JE  GCD_FOUND
    MOV AX, DX
    JMP COMPARE

SWAP_AX_BX:
    XCHG AX, BX
    JMP DIVIDE

GCD_FOUND:
    MOV GCD, BX

    MOV AX, NUM1
    MOV DX, 0
    MUL NUM2
    MOV BX, GCD
    DIV BX
    MOV LCM, AX

    MOV AH, 4CH
    INT 21H

CODE ENDS
END START
